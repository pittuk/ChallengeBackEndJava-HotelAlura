    private void jButtonContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonContinuarActionPerformed

        JDateChooser fechaIngreso = jDateChooserFechaIngreso;
        JDateChooser fechaSalida = jDateChooserFechaSalida;
        String formaPago = jComboBoxFormaPago.getSelectedItem().toString();
        Reserva reserva = new Reserva();
        try {
            reserva = reservaServicio.crearReserva(fechaIngreso, fechaSalida, formaPago);
        } catch (ParseException ex) {
            Logger.getLogger(RegistrarReserva.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (reserva != null) {
            //System.out.println(reserva);
            //damos paso al registro de huespedes 
            RegistrarHuesped registrarHuesped = new RegistrarHuesped(reserva);
            registrarHuesped.setVisible(true);
            registrarHuesped.setResizable(false);
            registrarHuesped.setLocationRelativeTo(null);
            dispose();
        } else {
            System.out.println("ha ocurrido un error");

        }


    }//GEN-LAST:event_jButtonContinuarActionPerformed

