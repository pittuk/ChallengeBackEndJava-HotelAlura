    private void jDateChooserFechaSalidaPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jDateChooserFechaSalidaPropertyChange

        try {
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
//obtenemos las fechas
            Date ingreso = formato.parse(((JTextField) jDateChooserFechaIngreso.getDateEditor().getUiComponent()).getText());
            Date salida = formato.parse(((JTextField) jDateChooserFechaSalida.getDateEditor().getUiComponent()).getText());
            Double precioReserva = reservaServicio.calcularPrecioReserva(ingreso, salida);
            jTextFieldMostrarValorReserva.setText(precioReserva.toString() + " CLP");
        } catch (Exception e) {
        }


    }//GEN-LAST:event_jDateChooserFechaSalidaPropertyChange

